package com.aipg.tunotify;

public class TUNotifyReq {
	private String MERCHANT_ID;
	private String ACCTNO;
	private String INACCT;
	private String OUTACCT;
	private String AMOUNT;
	private String MEMO;
	private String CUST_USERID;
	
	public String getMERCHANT_ID() {
		return MERCHANT_ID;
	}
	public void setMERCHANT_ID(String mERCHANTID) {
		MERCHANT_ID = mERCHANTID;
	}
	public String getACCTNO() {
		return ACCTNO;
	}
	public void setACCTNO(String aCCTNO) {
		ACCTNO = aCCTNO;
	}
	public String getINACCT() {
		return INACCT;
	}
	public void setINACCT(String iNACCT) {
		INACCT = iNACCT;
	}
	public String getOUTACCT() {
		return OUTACCT;
	}
	public void setOUTACCT(String oUTACCT) {
		OUTACCT = oUTACCT;
	}
	public String getAMOUNT() {
		return AMOUNT;
	}
	public void setAMOUNT(String aMOUNT) {
		AMOUNT = aMOUNT;
	}
	public String getMEMO() {
		return MEMO;
	}
	public void setMEMO(String mEMO) {
		MEMO = mEMO;
	}
	public String getCUST_USERID() {
		return CUST_USERID;
	}
	public void setCUST_USERID(String cUSTUSERID) {
		CUST_USERID = cUSTUSERID;
	}
	
	
}
